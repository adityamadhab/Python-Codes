#[], ordered(indexing), mutuable, heterogenous, allow duplicates
#Acces the elements in a list


list= ["Apple", 2.0, 10 ,"Banana"]
print(list)
print(list[1]) #2.0
print(list[-2]) #10
print(list[1:3]) #2.0, 10
print(list[-3:-1]) #2.0, 10
print(list[:3]) #Apple, 10
print(list[1:]) # 2.0, 10 , Banana
print(list[:-1]) #Apple, 2.0, 10
print(list[-3:]) #2.0, 10, Banana
